CREATE TABLE FHIR_GROUPBY_STATE \
	WITH (VALUE_FORMAT='AVRO') AS \
	SELECT GENDER as PATEINT_GENDER,	
		   ADDRESS[0]->state AS ADDR_STATE, \
           COUNT(*) AS CNT \
	FROM FHIR_AVRO_STREAM \
	GROUP BY ADDRESS[0]->state,GENDER;
CREATE TABLE FHIR_GROUPBY_STATE_SINK 
	WITH (VALUE_FORMAT='AVRO') AS 
	SELECT TIMESTAMPTOSTRING(ROWTIME, 'yyyy-MM-dd HH:mm:ss.SSS') AS TS, PATIENT_GENDER, ADDR_STATE, CNT FROM FHIR_GROUPBY_STATE;